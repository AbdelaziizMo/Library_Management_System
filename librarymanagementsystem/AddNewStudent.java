    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed

        if (student_id.getText().equals(""))
            JOptionPane.showMessageDialog(this, "Please enter Student ID");
        else if (name.getText().equals(""))
            JOptionPane.showMessageDialog(this, "Please enter Student Name");
        else if (section.getText().equals(""))
            JOptionPane.showMessageDialog(this, "Please enter Section");
        else if (year.getText().equals(""))
            JOptionPane.showMessageDialog(this, "Please enter Year Level");
        else if (semester.getText().equals(""))
            JOptionPane.showMessageDialog(this, "Please enter Semester");
        else{
            try {
                String studid = student_id.getText();
                String studname = name.getText();
                String studsec = section.getText();
                String studyear = year.getText();
                String studsem = semester.getText();
                
                stmt = con.prepareStatement("INSERT INTO student (student_id,name,section,year,semester)VALUES(?,?,?,?,?)");
                stmt.setString(1,studid);
                stmt.setString(2,studname);
                stmt.setString(3,studsec);
                stmt.setString(4,studyear);
                stmt.setString(5,studsem);
                
                int k =stmt.executeUpdate();
                if(k==1){
                   JOptionPane.showMessageDialog(this, "Student Information Successfully Added!!");
                   student_id.setText("");
                   name.setText("");
                   section.setText("");
                   year.setText("");
                   semester.setText("");
                }else{
                   JOptionPane.showMessageDialog(this, "Student Information Failed To Add!!");
                }
                
            } catch (SQLException ex) {
                Logger.getLogger(AddNewStudent.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

